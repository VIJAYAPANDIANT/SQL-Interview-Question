❓ How SQL is used in business analytics?

SQL is used to:

Extract data from databases

Aggregate metrics (sales, revenue, retention)

Build dashboards

Generate reports

Identify trends and patterns

It converts raw data → business insights.

❓ What defines a high-value customer?

A customer who:

Spends more than average

Purchases frequently

Generates high lifetime value

Usually measured by:

Total revenue

Purchase frequency

Recency

❓ Why time-based analysis is important?

Because business performance changes over time.

Helps to:

Identify trends

Forecast demand

Detect seasonal patterns

Track growth

Example: monthly revenue trend.

❓ SQL vs BI tools?
SQL	BI Tools
Query data	Visualize data
Data manipulation	Dashboards
Backend logic	Business reporting
Developer focused	Business user friendly

SQL prepares data → BI displays insights.

